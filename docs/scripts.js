var overrideBool = true;

function getGlyph(glyphIn){
    for (var i = 0; i < glyphList.length; i++){
        if (glyphList[i].glyphClass == glyphIn){
            return glyphList[i];
        }
    }
}

function createRule(glyphObj) {
    var rule = 'ul.list-' + glyphObj.glyphClass.substring(10) + ' li::before { \n\tcontent: "' + glyphObj.content + '";\n}\n\n';
    var overrideRule = 'li.list-' + glyphObj.glyphClass.substring(10) + '::before {\n\tcontent: "' + glyphObj.content + '" !important;\n}\n\n';
    var ruleObj = {
        css : rule,
        overrideCss : overrideRule,
        glyphId : glyphObj.glyphClass
    };
    return ruleObj;
}

function pushRule(rule) {
    textArea.push(rule);
    updateTextArea(overrideBool);
}

function removeRule(rule) {
    for (var i = textArea.length - 1; i >= 0; i--){
        if (textArea[i].glyphId == rule.glyphClass){
            textArea.splice(i, 1);
        }
    }
    updateTextArea(overrideBool);
}

function updateTextArea(override = true) {
    $('#outputText').val(function(){
        var output = '';
        for (var i = 0; i < textArea.length; i++){
            output += textArea[i].css;
        }
        if (override) {
            for (var i = 0; i < textArea.length; i++){
                output += textArea[i].overrideCss;
            }
        }
        return output;
    });

    console.log(textArea);
}

function checkboxRefresh() {
    $('.glyphicon-li > input').prop('checked', false);
    $('#includeOverride').prop('checked', true);
}


$(document).ready(function () {
    checkboxRefresh();

    updateTextArea(overrideBool);

    $('.glyphicon-li > input').on('change', function () {
        var element = $('label[for="' + $(this).attr('id') + '"]');
        console.log(element);
        var glyph = element.find('span.glyphicon')[0];
        console.log(glyph);
        console.log(glyph.classList[1]);
        if ($(this).is(':checked')) {
            pushRule(createRule(getGlyph(glyph.classList[1])));
        } else {
            removeRule(getGlyph(glyph.classList[1]));
        }
    });

    $('#includeOverride').on('change', function() {
        overrideBool = $(this).is(':checked');
        updateTextArea(overrideBool);
    });

    $('#search').on('keyup', function() {
        var searchTerm = $(this).val();
        $('.glyphicon-li').each(function(){
            var searchGlyph = $(this).find('span.glyphicon')[0];
            if (searchGlyph.classList[1].substring(10).indexOf(searchTerm) == -1) {
                $(this).hide();
            } else {
                $(this).show();
            }
        });
    });
});